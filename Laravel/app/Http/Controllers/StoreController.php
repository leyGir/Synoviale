     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Store  $store
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Store $store)
    {
        //
        $data = $request->only([
            'person_id',
        ]);

        $store->update($data);

        return redirect('store');
    }

